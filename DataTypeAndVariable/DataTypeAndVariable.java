public class DataTypeAndVariable {
    public static void main(String[] args) {
        //Primitive Data Types and variables.
        int a = 10;
        byte a1 = 25;
        long a2 = 8888;
        float b = 12.5f;
        double b1 = 125.625;
        char c = 'c';
        boolean d = true;
        System.out.println("Name " + a + "\n" + a1 + "\n" + a2 + "\n" + b + "\n" + b1 + "\n" + c + "\n" + d);    
        //String : is not a primitive data type, It is a derived data type.
        String s = "Shivam";
        System.out.println(s);
    }
}