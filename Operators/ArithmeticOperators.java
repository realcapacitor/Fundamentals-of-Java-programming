public class ArithmeticOperators {
    public static void main(String[] args) {
        //Additions
        int a = 25;
        int b = 43;
        int c = a + b;
        System.out.println(c);

        //Subtraction
        int d = 65;
        int e = 12;
        int f = d - e;
        System.out.println(f);

        //Multiplication
        int g = 12;
        int h = 21;
        int i = g * h;
        System.out.println(i);

        //Division
        int j = 12;
        int k = 5;
        float l = (float)j / (float)k;
        System.out.println(l);

        //Modulus
        int m = 15;
        int n = 4;
        int o = m % n;
        System.out.println(o);
    } 
}
